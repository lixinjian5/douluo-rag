# ── 分块参数 ──────────────────────────────────────────────────────────
# 500 中文字符 ≈ 300-350 tokens（中文每 token 约 1.5-2 字符）
# 所以 300-500 tokens ≈ 450-850 字符，取 500-750 稳妥
CHUNK_MIN_CHARS = 500
CHUNK_MAX_CHARS = 750
OVERLAP_CHARS = 100  # ≈ 60-70 tokens

# ── 分词估算 ──────────────────────────────────────────────────────────
def estimate_tokens(text: str) -> int:
    """粗略估算中文文本的 token 数。中文 ~1.5 char/token，英文 ~4 char/token。"""
    cn_chars = sum(1 for c in text if '一' <= c <= '鿿')
    other_chars = len(text) - cn_chars
    return int(cn_chars / 1.5 + other_chars / 4)
